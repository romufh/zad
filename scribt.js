function speakText() {
  let text = document.getElementById("inputText").value;
  let speech = new SpeechSynthesisUtterance();
  speech.lang = "ar-SA";
  speech.text = text;
  speech.rate = 1;
  speech.pitch = 1;
  window.speechSynthesis.speak(speech);
}
